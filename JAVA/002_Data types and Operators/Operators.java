public class Operators{
    public static void main(String[] args){
        float a = 10 + 20 * 30;
        float b = 100 / 10 * 100;
        float c = 5 * 4 / 4 % 3;
        float d = 100 + 200 / 10 - 3 * 10;
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}